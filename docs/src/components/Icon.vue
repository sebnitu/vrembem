<template>
  <span :class="rootClass" v-html="svg"></span>
</template>

<script lang="ts">
import { ref } from 'vue';
import feather from 'feather-icons';

export default {
  props: {
    name: {
      type: String,
      default: 'feather'
    },
    rootClass: {
      type: String,
      default: 'display-flex'
    },
    iconClass: {
      type: String,
      default: ''
    }
  },
  setup(props) {
    const svg = ref(null);
    const iconClass = `icon${(props.iconClass) ? ' ' + props.iconClass : ''}`;

    svg.value = feather.icons[props.name].toSvg({
      class: iconClass
    });

    return {
      svg
    }
  }
}
</script>
